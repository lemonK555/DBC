package com.kk.form;

public class CollectForm {
	String collectId;
	String productId;
	String productAddress;
	String productDescription;
	String productPriceUser;
	String userName;
	String collectInTime;
	
	
	public String getProductAddress() {
		return productAddress;
	}
	public void setProductAddress(String productAddress) {
		this.productAddress = productAddress;
	}
	public String getProductDescription() {
		return productDescription;
	}
	public void setProductDescription(String productDescription) {
		this.productDescription = productDescription;
	}
	public String getProductPriceUser() {
		return productPriceUser;
	}
	public void setProductPriceUser(String productPriceUser) {
		this.productPriceUser = productPriceUser;
	}
	public String getCollectId() {
		return collectId;
	}
	public void setCollectId(String collectId) {
		this.collectId = collectId;
	}
	public String getProductId() {
		return productId;
	}
	public void setProductId(String productId) {
		this.productId = productId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getCollectInTime() {
		return collectInTime;
	}
	public void setCollectInTime(String collectInTime) {
		this.collectInTime = collectInTime;
	}
}
