package com.kk.form;


public class PictureForm {
	String PID;
	String picId;
	String picName;
	String picFunctionName;
	String picIsChecked;
	String picAddress;
	String picReTime;
	
	
	public String getPID() {
		return PID;
	}
	public void setPID(String pID) {
		PID = pID;
	}
	public String getPicId() {
		return picId;
	}
	public void setPicId(String picId) {
		this.picId = picId;
	}
	public String getPicName() {
		return picName;
	}
	public void setPicName(String picName) {
		this.picName = picName;
	}
	public String getPicFunctionName() {
		return picFunctionName;
	}
	public void setPicFunctionName(String picFunctionName) {
		this.picFunctionName = picFunctionName;
	}
	public String getPicIsChecked() {
		return picIsChecked;
	}
	public void setPicIsChecked(String picIsChecked) {
		this.picIsChecked = picIsChecked;
	}
	public String getPicAddress() {
		return picAddress;
	}
	public void setPicAddress(String picAddress) {
		this.picAddress = picAddress;
	}
	public String getPicReTime() {
		return picReTime;
	}
	public void setPicReTime(String picReTime) {
		this.picReTime = picReTime;
	}
	
	
	
}
